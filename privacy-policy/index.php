<div class="content">
Hello!<br><br>
This is the non-wiki privacy policy of the Maamedia. This policy applies to non-Maamedia wikis, but some Maamedia products (such as social networks) may have different sections. You can see the privacy policy of Maamedia wikis <a href="https://meta.maamedia.org/wiki/Privacy_Policy">here</a>.
<br><br>
<h3>What information Maamedia collects when it visits its website</h3>
Whenever you visit any website of Maamedia (such as maamedia.org) or any other sites, including wikis, some information about you will be stored. This information is as follows:
    <li>IP address</li>
    <li>Browser</li>
    <li>Timezone and country</li>
    <li>Visiting time</li>
    <li>Pages where you visit</li>
This information is stored in a secret file, which can only be accessed via the command line with a password and cannot be found by a browser or robot. For example, if you are registered on a social network, more information about you will be stored, which you can find more on the <a href="https://maamedia.org/legal/">legal</a> page or on the social network policy pages.
<br><br>
<h3>What the data is used for</h3>
Non-personal information collected about you (such as pages you visit, time zone, and browser) is used to improve your user experience and to display the right content. Your visits are never told to outsiders, and no one has a right to know about it.
<br>
We use your information to personalize and check the services, which are the most popular pages and what should be on them.
<br>
<h3>How long the data is kept</h3>
Generally, your information on pages visited by Maamedia will be retained for three weeks until the file is cleaned and begins to collect new data. Data can also be retained for a much shorter period of time if the sites have several thousand visitors per day. <br>The file generally allows information for about 6,000 visitors, and the file will be automatically emptied whenever it is exceeded or no later than three (3) weeks. The visitor does not have the right to request the deletion of the data as it will happen automatically in a short time. If Maamedia were to start deleting the information of those who wanted it, it would become complicated and it would complicate the flow of information.
<br>
<h3>Who can see your information</h3>
Maamedia is still an exceptionally new project (founded at the end of January 2021). This means that Maamedia does not yet have many people, such as system administrators. <br>Currently, your information can only be viewed by the founders of Maamedia, who, of course, do not have the right to share your information with outsiders in non-emergency cases (listed below).
In other cases (if Maamedia had them), system and server administrators would be able to see your information.<br>
Your information can actually be viewed at any time on behalf of the founders, but this is not done. Usually, when you view your data, you are mainly looking at statistics and the most read pages. In addition, they design and make diagrams of what kind of content should be displayed more and what less, for example in different countries.
<br>
<h3>Where your information is stored</h3>
As mentioned above, your information is located in a separate file that can only be accessed with a password and command line languages. It is therefore impossible to open it to outsiders. The file does not contain any information that would be harmful to you or your privacy.
<br>
<h3>Emergencies where your information may be disclosed to other parties</h3>
There is a specific policy on when and to whom information collected about you may be disclosed. Listed here are so-called emergencies where your information may be revealed. 
<li>The IP address has been blocked from Maamedia wikis due to a violation of national law.</li>
<li>You have tried to leak illegal or vague material to our sites.</li>
<li>You make a visit to an area where Internet use is 1. restricted or 2. completely prohibited.</li>
<li>Your IP address matches a case that has already been notified to the parties.</li>
This information may be disclosed to following parties:
    <li>The local police organization in your country</li>
    <li>FBI</li>
    <li>To the community or organization concerned</li>
    <li>For a lawyer</li>
    <li>If you have an open proxy (VPN), the IP address you use will be reported for abuse</li><br>
The information will be disclosed by e-mail and, if necessary, screenshots or complete passwords will be sent to access Maamedia servers.<br>
<h3>Changes to this Privacy Policy</h3>
Maamedia, its founders and the Privacy Policy Officer (currently the founders) have the right to modify this policy without notice.<br><br>

Last modified: 6. May 2021
