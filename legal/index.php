<h1>Area</h1>
Applies to all Maamedia sites, wikis, tools, and social networks, <b>unless it has been modified afterwards and is clearly stated.</b><br>
This document is subject to change and there is no obligation to notify it in advance. <br>The document is legal and contains Maamedia's legal and liability information. This only applies to Maamedia products mentioned above and at the beginning of the document.
<h1>Maamedia products</h1>
Maamedia's products include all the websites and tools created by Maamedia or its founders or voluntary participants in the project.<br>

Maamedia products include all <b>Powered by Maamedia</b>-tagged wikis (such as Masymanwiki), all free web software and utilities printed under <b>Powered by Maamedia Tools</b>, or social networks (such as Mawonder) printed by <b>One of Maamedia Community Services</b> - marked.
Anyone is eligible to make Maamedia products, and all products and their creators are a kind of Maamedia family. Only those made with Maamedia in mind are counted as Maamedia products. Maamedia products may also be available in app stores, where they are identified by the publisher name “<b>Maamedia Technology</b>”.<br> If you see any tool or software that is claimed to be made by Maamedia (even if it includes the Powered By label), you may want to ask Maamedia if you suspect.<br> All tools made by Maamedia are usually identified by the .maamedia.org domain. <br>Masymanwiki is a manual recognized by the .masymanwiki.org domain

The use, publication and making of Maamedia products is voluntary and does not constitute an obligation.<br> All services can be used free of charge, although some products / services may allow so-called “in-app purchases”, such as on the social network Mawonder. <br>Mawonder's terms of use can be found at social.maamedia.org, and this document also mentions Maamedia’s general purchase and payment policy

<h1>Legality of Maamedia</h1>
Maamedia is not registered in any country as a company, organization, campaign, organization, association or foundation.<br> So Maamedia is not a company in any country, and this is well reflected in all Maamedia documents and practices. Maamedia is run with the help of the community; the community allows Maamedia to be maintained.<br> Maamedia’s domain names and other expenses are paid for by Maamedia founders, but in some countries, a donation option may be introduced.<br> This is done through the PayPal payment application. The document later mentions where the donations received go and what rights the donor has.
Maamedia does not engage in business activities and does not pay anyone for anything.<br> You cannot work in Maamedia except for volunteer work, which includes 1. editing wikis and other products, 2. software development or maintenance, 3. administrative tasks or fund management, 4. advertising.<br> The founders of Maamedia from Finland and Indonesia have voluntarily established Maamedia voluntarily. 

The content of the Maamedia has been made to be in line with the content policy of each state on Earth and with what can be shown in each country.<br> However, while in some borderline cases, the content of the Maamedia may conflict with state regulations, here it is usually a matter of a Masymanwiki article or its writing. <br>In China, for example, there are precise boundaries with social media services and internet use. Thus, Maamedia has sought to reduce its communications in China and the Chinese administrative regions, although it is possible to operate there.

<h1>Legal status of Maamedia</h1>
Maamedia wikis are very risky for cases that are at a level where the case may belong to an authority. Maamedia is not responsible for what is written on the wiki or its social network, but is responsible for, among other things, the safety and protection of users. <br><br>Maamedia removes from wikis all things created to 1. offend another person or company, 2. incite against a group of people, 3. publish criminal material or things to keep secret, 4. threaten others with violence, 5. tell about a user's plans that break the law, 6 to incite criminal activity or crime.
<br><br>Maamedia is obliged to notify the authority of that country if its services are visible or suppressed: 
<li>material that occurs under the age of 18,</li>
<li>sexual exploitation,</li>
<li>weapons, drugs, corpses or instrumentalities,</li>
<li>technical or direct plan for the offense,</li>
<li>confession of the offense and the evidence to which it relates,</li>
<li>incitement to criminal activity,</li>
<li>incitement against a group of people,</li>
<li>animal abuse,</li>
<li>death threats or confessions,</li>
<li>material suggesting genocide,</li>
<li>content prohibited by law.</li>

The leaders of the Maamedia, ie the founders, <b>always</b> report matters to the authorities of the country in which the case has arisen or is concerned.

The founders may become aware of the case 1. by seeing it themselves (considerably speeding up the notification) or 2. by e-mail notification (the matter will be brought to the attention of the authorities, but it may take a while to receive the e-mail due to congestion). <br>Publicly, such cases must not be told to anyone or any channel to publish anything.
<h1>What information does Maamedia collect about its users</h1>
Maamedia collects some information about users on wikis or social networks.<br> Slightly more data is collected from registered users of the service, and less is collected from so-called IP visitors. <br><br>However, all users are always collected:
<li>IP address</li>
<li>Telecom operator</li>
<li>Browser (Google/Mozilla etc.)</li>
<li>Country visited (if visited with a business IP address, the city or even address may also be displayed)</li>
<br>
The following information is collected from users registered on the wiki:
    <br>
<li>IP address</li>
<li>Telecom operator</li>
<li>Browser (Google/Mozilla etc.)</li>
<li>Country visited (if visited with a business IP address, the city or even address may also be displayed)</li>
<li>Age, if set by the user</li>
<li>Email, if set by the user</li>
<li>First and last name, if set by the user</li>
<li>Timezone</li>
<li>Registration time</li><br><br>
Whenever an edit or log function (such as moving or deleting a page) is performed on the wiki, a trace is left in the address bar.<br><br> Address Revealer is a secure tool that can be used by founders, wiki administrators, and address revealer users. The tool displays the IP addresses of all registered users who have been logged or edited.<br> In addition, the tool can retrieve a specific IP address and find out if it has been modified for Maamedia wikis or if it has a previous wiki account.<br><br> Address bar users are very reliable and have a common policy that can be read on Maamedia's Meta-Wiki.
The user does not usually have the right to ask or retrieve from Maamedia the information that Maamedia has had time to collect by a certain time. <br><br>However, this has become new to the Mawonder social network, meaning that in Mawonder the user is able to create a file into which the user selects the information that Maamedia has collected and which the user wants to see.
<br>
<h1>Where the data is stored</h1>
The data of all users goes to Maamedia database, which is not accessible to third parties. <br>When going to the database, more checks, authentications and different passwords are always required. <br><br>The database contains a person who has made an edit or publication (or registration) on everyone's wiki or other Maamedia product, showing the information listed above or less.<br><br> The database is secured and protected, and it has day-to-day monitoring.
Data is currently accessible to the database only by the founders of Maamedia, but in the future also by the administrators of the server. <br>User data will not be viewed unless there is a compelling reason to do so, and the user will always be notified.

Data of unregistered persons, i.e. visitors, is stored in a single, weekly cleaned file located deep on the servers, which is also impossible to access. <br><br>However, access to everything is always up to the founders and server administrators. 
<h1>How long the data is kept</h1>
The data is usually stored in the database for ten months at a time. If the registered person makes publications, edits or visits, his data will not be lost after the next ten months.<br> If he makes edits throughout his life, the data will never be sourced from the database.
Visitor data, on the other hand, is cleared weekly, as said above.<br><br> In this case, the file will be emptied and it will be ready to fill again. File information is lost from the entire server system.

<h1>Payments and purchases</h1>
Some Maamedia services, such as Mawonder 2021, will be able to make in-app purchases, which means the user will be able to subscribe to the services. At Mawonder you can buy VIP memberships for real money. Purchases and payments are made possible with the PayPal payment application. <br><br>Similarly, Maamedia’s donations, which will become possible during June 2021, will be made through PayPal.

If you wish, you will receive a receipt for the purchase. The buyer (or donor) also has the right to know where his money has gone.

Maamedia guarantees that all donations and payments received will be used for the maintenance and upkeep of the pages and servers.<br><br> Maamedia survives on servers, and the server must have a large amount of space, or memory. 50 percent of the payments you receive go to the purchase of memory and the rest to the maintenance of services, speed and websites, and the cost of domains. <br><br>Maamedia keeps a record on Meta-Wiki of where Maamedia spends money, how much money has been spent on each purchase, and also keeps a record of the payments and donations it receives on the same page. The page can be found as “Payment history” but is only edited by the founders or treasurers (the founders are currently also treasurers).
Maamedia does not aim for operating profit or turnover and does not compete with others. Maamedia accepts donations from both private individuals and companies. Maamedia considers the maximum donation amount to be 30 euros per person / company. <br><br>It would be a good idea to donate to the same person or company only once every three weeks.
The buyer or donor has the right to know where his money has gone. Information on these issues can be requested from the founders of Maediaia either by emailing <code>mito@maamedia.org</code> or <code>puma@maamedia.org</code>. <br>Receipts and other information can be sent to applicants if they do not understand or want to read the Payment history page in Meta.
<h1>Maamedia languages</h1>
Maamedia is largely multilingual, with a few exceptions. The website maamedia.org is written in many different languages and can still be translated.<br> Translations are often done in Meta-Wiki using the Translate action page. Wikis are all multilingual because MediaWiki software is developed in all the languages of the world. <br><br>New translations can be made freely on Translatewiki.net, which is edited every day and from where new translations are also brought to Maamedia wikis every week.
<br><br>
Maamedia documents are multilingual or at least written in several languages. Typical languages used in documents are at least English, Finnish and Indonesian.

For example, Maamedia displays its website in English by default, while the wiki has a setting that identifies the users country and goes according to the language of that country.<br><br> This is aided by the IP address and state (see section 4: <b>What information Maamedia collects about its users</b>).

Maamedia has a number of multilingual Masymanwiki wikis, some of which, however, are closed. <br>On April 25, 2021, there are Masymanwiki in Finnish, English, and Indonesian, as well as Commons-Wiki, Data-Wiki, Test-Wiki, and Meta-Wiki, all of which are in English.<br><br> The tools are also in English.
<br><br>

<h1>Ages</h1>

<h3>SOCIAL NETWORKS</h3>
Social networks like Mawonder have a minimum age of 13 years. This is to protect children under the age of 13 from content that does not belong to them. There is no age limit on wikis, but some of its user groups (such as the address revealer) have age limits. <br>Below is more information about wiki age limits.
<br><br>
In a social network, it would be good for people under the age of 15 to also browse it under the supervision of their parents.<br> It is possible to write anything on a social network and they can show anyone almost anything, even if the sites have enhanced screening and verification.

In addition, social networks usually have the ability to chat with another person via the chat feature and these messages are not seen by anyone other than the sender and recipient themselves. Anyone can join a group chat unless it's changed in the settings.
<br>
Social networks are not a club search site and may not sell clubs. Instead, products such as phones or clothing may be sold. <br>For more information on Mawonder's features, see the separate documents and the Meta-Wiki page.
<br><br>
<h3>WIKIS</h3>
There is no upper or lower age limit for Maamedia wikis. This means that everyone has the right to 1. edit wikis, 2. access certain user groups on wikis, and 3. read wikis.<br><br>
Editing means the right that an unregistered user also has. IP addresses have the right to edit unprotected pages on Maamedia wikis.<br>

Access to certain user groups means the right that a registered user with their experience and knowledge of policies can gain, for example, administrator rights. However, some user groups, such as the CheckUser, Steward, or Overisghter (Suppressor), have an 18-year age limit.<br><br> This matter will be verified by the applicant / proposed sending an identity card or other valid certificate to the founders of Maamedia.<br><br> Otherwise, this information should be sent to Maamedia Data Protection Review Committee or Security Committee, which will also state on the wiki that the user is over 18 years of age (but does not indicate age, name or other personal information).

<br><br>
Reading wikis means the right to read wiki pages or other information that is displayed on the wiki. Pages cannot be protected from reading, but can be configured from the wikis internal settings.<br> However, this will never be done on Maamedia wikis, because Maamedia is open to everyone.
<br>
<h3>TOOLS</h3>
Maamedia tools have no lower or upper age limit, so it can be used by people of all ages.

<br>
The tools are designed to make people’s lives and daily lives easier, as well as for companies and organizations such as payroll or time management.<br>
As a result, the tools do not have any content or functionality that could not be displayed to everyone.<br><br>
<h3>APPLICATIONS</h3>
The age limit of a Maamedia application depends on the application.<br> The tool applications are not age-restricted, meaning anyone can use them, but Mawonder mobile application, for example, has the same age limit as the online version, ie 13 years.
<br><br>
<h3>OTHER MAAMEDIA PRODUCTS</h3>
The developer of Maamedia can be of any age, but the server administrator usually has to be a little older.<br>
The OTRS service used by Maamedia, where any Maamedia customer or other user can ask more about the services. The OTRS queue also includes permit inquiries, e.g. About Maamedia logos. The so-called OTRS service the customer service representative, or OTRS member, must be over 16 years of age. <br><br>At this point, Maamedia has to check the age.
<br>
<h1>AGE CONTROL</h1>
Maamedia <b>cannot take responsibility for who or what age a person uses the product</b>. There is no control whatsoever, except in a few exceptional cases. <br>As mentioned above, in certain cases, a wiki must be able to somehow prove that they are over 18 years of age.
<br><br>
Social networks like Mawonder ask about age when registering. However, it is good to keep in mind that you can write anything on it and claim to be older, even if you are 9 years old, for example. <br>Maamedia does not check the age of its users in the Mawonder application or elsewhere, unless there is a clear need for it or its own policy.
<br>
<br><h1>MAAMEDIA LOGOS</h1>
Maamedia logos are logos, ie images, created for Maamedia and its products and services. The Masymanwiki logo is one of Maamedia's logos. Maamedia logos are only used in Maamedia's services and products.<br> Logos can be developed by anyone, but that doesn’t immediately mean they would ever be used on Maamedia.<br><br>
The use of logos in our own, non-Maamedia projects is permitted under certain conditions. The movement is started by sending a message to the OTRS service, where the message goes to permission requests, i.e. the so-called queue. <br>When the requester is on his or her turn, the OTRS member will respond to him or her and ask for more of his or her needs. <br>The OTRS service is either authorized or denied permission to use the Maamedia logo.
<br><br>
Permission requests will only be sent to  OTRS service if someone is using Maamedia logos in their own projects, which are seen by more than ten (10) people. If the logo is seen by less than ten people in a non-Maamedia product / service, permission is not required.<br><br>
In connection with the permit, it must be stated precisely why you want to use logo, what it does in the project, how many people could have the opportunity to see the logo and what is the idea of your own project. Too weak answers, ie justifications, make it difficult to grant a permit.
<br><br>
If you obtain permission from OTRS, you may use logo or logos in the matters you mentioned in the post.<br> You should attach information about where the logo came from (<b>Maamedia Commons</b> commons.maamedia.org) and the OTRS permit application number next to the logo. <br>You will receive the permit request number after receiving the permit. <br><br>The number allows anyone to check if  OTRS member has granted permission.
<br>
Maamedia logos can be found on the Maamedia Commons-Wiki, where you can download them however you want and where you want them. However, their (re) use is not permitted in Maamedia regulations without permission. <br>If you see the Maamedia logo on a site (and it is not properly mentioned or the logo is not on .maamedia.org or .masymanwiki.org), this is not a permitted case. You should always report the issue to OTRS or some of the founders.

This policy (which is given above) is valid until June 2021, when new policies for Maamedia logos will take effect. <br><br>They are:
<br>
Logos may be used without mention or permission. Logos can also be customized for your own use. <br>However, it is a good idea to mention the original author of the logo and the place where the logo was taken.

Originally, a logo policy was created to prevent the use of logos without permission. However, this was found to be futile because anyone can download the Maamedia logo from the Commons and use it.<br>
